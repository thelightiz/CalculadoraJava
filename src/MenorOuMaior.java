public class MenorOuMaior {
    public static void main(String[] args) {
        int numero = -10;
        if (numero > 0) {
            System.out.println("Maior que 0");
        }
        else if (numero < 0) {
            System.out.println("Menor que 0");
        } else {
            System.out.println("Igual a 0");
        }
    }
}